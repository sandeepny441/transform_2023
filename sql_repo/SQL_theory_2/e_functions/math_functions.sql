-- Absolute Value
SELECT ABS(-15);

-- Power
SELECT POWER(2,3); 
-- 8

-- Square Root  
SELECT SQRT(25);

-- Ceiling
SELECT CEILING(4.2);

-- Floor
SELECT FLOOR(4.7);

-- Round
SELECT ROUND(4.4);

-- Round to decimals
SELECT ROUND(34.255, 2);

-- Modulo
SELECT MOD(10, 3);

-- Random 
SELECT RAND();

-- Random within range
SELECT RAND(1); 

-- Trig functions  
SELECT SIN(RADIANS(30)), COS(RADIANS(60)), TAN(RADIANS(45));

-- Logarithms
SELECT LOG(100), LN(2.7);

-- Exponents
SELECT EXP(2);
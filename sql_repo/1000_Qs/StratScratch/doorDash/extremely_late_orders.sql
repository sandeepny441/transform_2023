To remain competitive,
the company you work with must reduce the 
number of extremely late deliveries.

A delivery 
is flagged as extremely late if the actual delivery 
time is more than 20 minutes (not inclusive)
after the predicted delivery time.

You have been asked to calculate the percentage of 
orders that arrive extremely late each month.

Your output should include the month in the format 'YYYY-MM'
and the percentage of extremely late orders as 
a percentage of all orders placed in that month.